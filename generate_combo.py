def gen_api_key():
    styles = [
        lambda: "sk-" + "".join(random.choices(string.ascii_letters + string.digits, k=40)),
        lambda: "bk_api_key_" + "".join(random.choices(string.ascii_uppercase + string.digits, k=24)),
        lambda: "Bearer " + "".join(random.choices(string.ascii_letters + string.digits, k=32)),
        lambda: "".join(random.choices(string.ascii_lowercase + string.digits, k=10)) + ":" + "-".join(
            random.choice("xyzw") + "-" + str(random.randint(0, 9))
            for _ in range(random.randint(15, 25))
        ),
        lambda: "'new_project_dev_key_" + "".join(random.choices(string.ascii_lowercase, k=5)) + "'",
    ]
    return random.choice(styles)()


def gen_password():
    chars = string.ascii_letters + string.digits + "!@#$%"
    pwd = "".join(random.choices(chars, k=random.randint(8, 16)))
    styles = [
        lambda: pwd,
        lambda: f'"{pwd}"',
        lambda: pwd.replace(random.choice(pwd), random.choice("@#$"), 1),
    ]
    return random.choice(styles)()


