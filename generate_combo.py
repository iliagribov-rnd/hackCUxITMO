def generate_single_example(label: str | None = None) -> dict:
    """Генерирует single-label пример в стиле реального датасета."""
    if label is None:
        labels = list(LABEL_WEIGHTS.keys())
        weights = [LABEL_WEIGHTS[l] for l in labels]
        label = random.choices(labels, weights=weights, k=1)[0]

    templates = SINGLE_TEMPLATES.get(label)
    if not templates:
        label = random.choice(list(SINGLE_TEMPLATES.keys()))
        templates = SINGLE_TEMPLATES[label]

    template_str, gen_fn = random.choice(templates)
    val = gen_fn()

    ph_str = "{val}"
    idx = template_str.index(ph_str)
    text = template_str[:idx] + val + template_str[idx + len(ph_str):]
    spans = [(idx, idx + len(val), label)]

    ctx = random.choice(DIALOG_CONTEXTS)
    prefix_len = ctx.index("{}")
    text = ctx.format(text)
    spans = [(s + prefix_len, e + prefix_len, lbl) for s, e, lbl in spans]

    target_str = "[" + ", ".join(f"({s}, {e}, '{lbl}')" for s, e, lbl in spans) + "]"
    entities = [text[s:e] for s, e, lbl in spans]

    return {
        "text": text,
        "target": target_str,
        "entity": str(entities),
    }


def generate_combo_example() -> dict:
    """Генерирует пример с 2–4 ПД из COMBO_TEMPLATES."""
    template, ph_map = random.choice(COMBO_TEMPLATES)
    ordered_phs = re.findall(r"\{(\w+)\}", template)

    values = {ph: fn() for ph, (fn, _) in ph_map.items()}

    text = template
    spans = []
    for ph in ordered_phs:
        val = values[ph]
        label = ph_map[ph][1]
        ph_str = "{" + ph + "}"
        idx = text.index(ph_str)
        text = text[:idx] + val + text[idx + len(ph_str):]
        spans.append((idx, idx + len(val), label))

    ctx = random.choice(DIALOG_CONTEXTS)
    prefix_len = ctx.index("{}")
    text = ctx.format(text)
    spans = [(s + prefix_len, e + prefix_len, lbl) for s, e, lbl in spans]

    target_str = "[" + ", ".join(f"({s}, {e}, '{lbl}')" for s, e, lbl in spans) + "]"
    entities = [text[s:e] for s, e, lbl in spans]

    return {
        "text": text,
        "target": target_str,
        "entity": str(entities),
    }


def generate_negative_example() -> dict:
    template = random.choice(NEGATIVE_TEMPLATES)
    text = template
    for ph, fn in _NEG_GENS.items():
        while "{" + ph + "}" in text:
            text = text.replace("{" + ph + "}", fn(), 1)
    ctx = random.choice(DIALOG_CONTEXTS)
    text = ctx.format(text)
    return {"text": text, "target": "[]", "entity": "empty"}


