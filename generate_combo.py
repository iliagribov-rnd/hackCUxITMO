def generate_df(
    n_total: int = 5000,
    combo_frac: float = 0.3,
    neg_frac: float = 0.25,
) -> list[dict]:
    """
    Генерирует датафрейм с тремя типами примеров:
    - single-label (основная масса, как в реальном датасете)
    - combo (мульти-лейбл)
    - negative (без ПД)
    """
    n_neg = round(n_total * neg_frac)
    n_combo = round(n_total * combo_frac)
    n_single = n_total - n_neg - n_combo

    rows = []
    rows += [generate_single_example() for _ in range(n_single)]
    rows += [generate_combo_example() for _ in range(n_combo)]
    rows += [generate_negative_example() for _ in range(n_neg)]
    random.shuffle(rows)

    return rows


def main():
    parser = argparse.ArgumentParser(description="Generate realistic NER training data")
    parser.add_argument("--n_total", type=int, default=12000, help="Total examples")
    parser.add_argument("--combo_frac", type=float, default=0.3, help="Fraction of combo (multi-label)")
    parser.add_argument("--neg_frac", type=float, default=0.25, help="Fraction of negatives")
    parser.add_argument("--output", default="generated.tsv", help="Output TSV")
    args = parser.parse_args()

    rows = generate_df(n_total=args.n_total, combo_frac=args.combo_frac, neg_frac=args.neg_frac)

    with open(args.output, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=["text", "target", "entity"], delimiter="\t")
        writer.writeheader()
        writer.writerows(rows)

    import ast
    height = len(rows)
    label_counts = Counter()
    combo_sizes = Counter()
    unique_combos: set[tuple[str, ...]] = set()
    n_neg = 0

    for row in rows:
        spans = ast.literal_eval(row["target"])
        if not spans:
            n_neg += 1
            continue
        labels = tuple(sorted(set(s[2] for s in spans)))
        combo_sizes[len(labels)] += 1
        if len(labels) > 1:
            unique_combos.add(labels)
        for s, e, lbl in spans:
            label_counts[lbl] += 1

    print(f"Сгенерировано: {height} примеров")
    print(f"  Single-label: {sum(v for k,v in combo_sizes.items() if k == 1)}")
    print(f"  Combo (multi-label): {sum(v for k,v in combo_sizes.items() if k > 1)}")
    print(f"  Negative: {n_neg}")

    print(f"\n  Размеры комбо:")
    for size, cnt in sorted(combo_sizes.items()):
        print(f"    {size} лейбл(а/ов): {cnt}")

    print(f"\n  Уникальных комбинаций: {len(unique_combos)}")

    print(f"\n  Покрытие лейблов ({len(label_counts)}/30):")
    for lbl, cnt in label_counts.most_common():
        print(f"    {lbl:<80} {cnt:>5}")

    lens = [len(r["text"]) for r in rows]
    print(f"\n  Длина текста: min={min(lens)}, max={max(lens)}, "
          f"mean={sum(lens)/len(lens):.0f}, median={sorted(lens)[len(lens)//2]}")


if __name__ == "__main__":
    main()
