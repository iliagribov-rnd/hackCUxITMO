def generate_df(
    n_total: int = 4000,
    hard_neg_frac: float = 0.30,
    hard_pos_frac: float = 0.40,
    combo_frac: float = 0.30,
) -> list[dict]:
    n_neg = round(n_total * hard_neg_frac)
    n_pos = round(n_total * hard_pos_frac)
    n_combo = n_total - n_neg - n_pos

    rows = []
    rows += [generate_hard_negative() for _ in range(n_neg)]
    rows += [generate_hard_positive() for _ in range(n_pos)]
    rows += [generate_combo_example() for _ in range(n_combo)]
    random.shuffle(rows)
    return rows


def main():
    import ast as ast_mod

    parser = argparse.ArgumentParser(description="Generate edge-case NER training data")
    parser.add_argument("--n_total", type=int, default=4000)
    parser.add_argument("--hard_neg_frac", type=float, default=0.30)
    parser.add_argument("--hard_pos_frac", type=float, default=0.40)
    parser.add_argument("--combo_frac", type=float, default=0.30)
    parser.add_argument("--output", default="generated_edge.tsv")
    args = parser.parse_args()

    rows = generate_df(
        n_total=args.n_total,
        hard_neg_frac=args.hard_neg_frac,
        hard_pos_frac=args.hard_pos_frac,
        combo_frac=args.combo_frac,
    )

    with open(args.output, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=["text", "target", "entity"], delimiter="\t")
        writer.writeheader()
        writer.writerows(rows)

    # --- Статистика ---
    label_counts = Counter()
    combo_sizes = Counter()
    n_neg = 0
    span_lens = []

    for row in rows:
        spans = ast_mod.literal_eval(row["target"])
        if not spans:
            n_neg += 1
            continue
        labels = tuple(sorted(set(s[2] for s in spans)))
        combo_sizes[len(labels)] += 1
        for s, e, lbl in spans:
            label_counts[lbl] += 1
            span_lens.append(e - s)

    print(f"Сгенерировано: {len(rows)} примеров → {args.output}")
    print(f"  Hard negatives:   {n_neg}")
    print(f"  Hard positives:   {sum(v for k,v in combo_sizes.items() if k == 1)}")
    print(f"  Combo (multi-label): {sum(v for k,v in combo_sizes.items() if k > 1)}")

    print(f"\n  Combo sizes:")
    for size, cnt in sorted(combo_sizes.items()):
        print(f"    {size} label(s): {cnt}")

    print(f"\n  Unique combos: {len(set(tuple(sorted(set(s[2] for s in ast_mod.literal_eval(r['target'])))) for r in rows if r['target'] != '[]'))}")

    print(f"\n  Label coverage ({len(label_counts)}/30):")
    for lbl, cnt in label_counts.most_common():
        print(f"    {lbl:<75} {cnt:>5}")

    if span_lens:
        short = sum(1 for l in span_lens if l < 4)
        print(f"\n  Span lengths: min={min(span_lens)}, max={max(span_lens)}, "
              f"mean={sum(span_lens)/len(span_lens):.0f}, short(<4)={short}")

    text_lens = [len(r["text"]) for r in rows]
    print(f"  Text lengths: min={min(text_lens)}, max={max(text_lens)}, "
          f"mean={sum(text_lens)/len(text_lens):.0f}")


if __name__ == "__main__":
    main()
