def _gen_text_date_prep():
    """Дата в предложном падеже (в январе 2025)."""
    d = _rnd_date(2024, 2027)
    fmts = [
        f"в {_MONTHS_PREP[d.month]} {d.year}",
        f"в {_MONTHS_PREP[d.month]} {d.year} года",
    ]
    return random.choice(fmts)


