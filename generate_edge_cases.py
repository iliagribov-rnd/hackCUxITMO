"""
Генератор «трудных» примеров для обучения NER-модели.

Покрывает проблемы, выявленные в edge_cases.md:

1. HARD NEGATIVES — тексты, которые выглядят как positive, но НЕ содержат ПД:
   - даты текстом в общем контексте (не entity)
   - названия стран в контексте визы/путешествий (не entity)
   - упоминания банков как бренда (не entity)
   - слова «пароль», «ПИН», «код» без конкретного значения
   - время (10:00, 07:07), похожее на даты/коды

2. HARD POSITIVES — тексты с корректными, но трудными entity:
   - CVV = 3 цифры (модель не должна обрезать)
   - аббревиатуры стран (США, ОАЭ, ЮАР) — корректный entity
   - короткие банки (ВТБ, МТС) — корректный entity
   - даты текстом как entity (целиком, не фрагментами)
   - полные ИНН/паспорта (не обрезать начало/конец)
   - полные названия авто (Geely, не Ge+ely)
   - кодовые слова из 3 символов (Мир, Лес)

3. COMBO (multi-label) — реальные комбинации ПД в одном тексте

4. UNIQUE TEST PATTERNS — паттерны из тестовых текстов, которых нет в train:
   - EMV-чип, криптография, скимминг
   - JWT-токены (eyJhbG...)
   - Серийные номера устройств (Interface Device Serial Number)
   - Политика ротации API ключей
   - Специфические авто (Honda Civic, Volvo XC60) + VIN/двигатель/шасси
   - Уникальные ФИО (Айтматова, Курбанова, Мухаметов)
   - Специфические улицы (Оренбургский тракт, Плехановская, Гончарова)
   - Пенсионные / валютный контроль / залоговое имущество
   - 2-НДФЛ

Запуск:
    python generate_edge_cases.py --n_total 4000 --output generated_edge.tsv
"""

