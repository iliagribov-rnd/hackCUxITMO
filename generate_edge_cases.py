def generate_hard_negative() -> dict:
    template = random.choice(HARD_NEGATIVE_TEMPLATES)
    text = template
    for ph, fn in _HARD_NEG_GENS.items():
        while "{" + ph + "}" in text:
            text = text.replace("{" + ph + "}", fn(), 1)
    return {"text": text, "target": "[]", "entity": "empty"}


