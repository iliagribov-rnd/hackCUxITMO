def gen_birth_date_full_text():
    """Дата рождения текстом ЦЕЛИКОМ (не фрагментированная)."""
    d = _rnd_date(1955, 2005)
    fmts = [
        f"{d.day} {_MONTHS_GEN[d.month]} {d.year} года",
        f"{d.day} {_MONTHS_GEN[d.month]} {d.year}",
        f"{d.day:02d} {_MONTHS_GEN[d.month]} {d.year} года",
    ]
    return random.choice(fmts)


def gen_birth_date_numeric():
    """Дата рождения в числовом формате."""
